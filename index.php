            <h3>A ORIGEM DO KARATÊ</h3>

            <p>Karatê-dô (caminho da mão vazia) ou, simplesmente, Karatê, é uma forma de budo (caminho marcial). Arte marcial japonesa originária de Okinawa, foi introduzida nas principais ilhas do arquipélago japonês em 1922. O Karatê enfatiza as técnicas de percussão atemi waza (i.e. defesas, socos e chutes) ao invés das técnicas de projeções e imobilizações. O treino de Karatê pode ser dividido em três partes principais: Kihon, Kata e Kumite.
Kihon (fundamentos) é o estudo dos movimentos básicos.
Kata (forma, padrão) é uma espécie de luta contra um inimigo imaginário expressa em seqüências fixas de movimentos.
Kumite (encontro de mãos) é a luta propriamente dita. Em sua forma mais básica é combinada (com movimentos pré-determinados) entre os lutadores para, posteriormente, alcançar o jyu kumite (combate livre ou sem regras).
O Karatê esportivo, ou combate com regras, é conhecido como Shiai-kumite.</p>

